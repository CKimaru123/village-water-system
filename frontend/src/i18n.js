// Languages that should use RTL direction
const RTL_LANGS = ['ar', 'he', 'fa', 'ur'];

// Ensure document language and direction are set on load
const applyDocumentLang = (lng) => {
  try {
    const short = (lng || i18n.language || 'en').slice(0,2);
    document.documentElement.lang = short;
    document.documentElement.dir = RTL_LANGS.includes(short) ? 'rtl' : 'ltr';
  } catch (e) {
    // ignore in non-browser environments
  }
};

applyDocumentLang(i18n.language);

// React to language changes globally
i18n.on('languageChanged', (lng) => {
  applyDocumentLang(lng);
});

